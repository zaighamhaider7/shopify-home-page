function darkmode(){
    document.getElementById("body").style.backgroundColor="black"
    document.getElementById("body").style.color="white"
    document.getElementById("modes").style.backgroundColor="black"
    document.getElementById("modes").style.transition="0.8s"
    document.getElementById("login").style.backgroundColor="green"
    document.getElementById("free").style.backgroundColor="green"
    document.getElementById("secndfreetrail").style.backgroundColor="green"
}
function lightmode(){
    document.getElementById("body").style.backgroundColor="skyblue"
    document.getElementById("body").style.color="black"
    document.getElementById("modes").style.backgroundColor="white"
    document.getElementById("modes").style.transition="0.8s"
    document.getElementById("login").style.backgroundColor="black"
    document.getElementById("free").style.backgroundColor="black"
    document.getElementById("secndfreetrail").style.backgroundColor="black"
}